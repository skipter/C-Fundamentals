namespace _02.PointInRectangle
{
    public class Rectangle
    {
        private int topLeft;
        private int bottomRight;

        public int BottomRight
        {
            get { return this.bottomRight; }
            set { this.bottomRight = value; }
        }


        public int TopLeft
        {
            get { return this.topLeft; }
            set { this.topLeft = value; }
        }

    }
}
